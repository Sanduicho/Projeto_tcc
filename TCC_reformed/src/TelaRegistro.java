    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String sobrenome = txtSobrenome.getText();
        String usuario = txtUsuario.getText();
        String senha = String.copyValueOf(cmpSenha.getPassword());
        String resenha = String.copyValueOf(cmpReSenha.getPassword());
        
        if(usuario.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor, digite um nome de usuario");
        }else
        
        if(senha.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor, digite uma senha");
        }else
        
        if(!senha.equals(resenha)){
            JOptionPane.showMessageDialog(null, "Por favor, digite a senha novamente");
        }
        
        PreparedStatement ps;
        String query = "insert into `administrador_hotel` (`Nome`,`Sobrenome`,`Usuario`,`Senha`) VALUES (?,?,?,?)";
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, nome);
            ps.setString(2, sobrenome);
            ps.setString(3, usuario);
            ps.setString(4, senha);
            
            if(ps.executeUpdate() > 0){
                JOptionPane.showMessageDialog(null, "New User Added");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegistroActionPerformed




    

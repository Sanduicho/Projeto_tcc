    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        PreparedStatement ps = null;
        ResultSet rs;

        String usuario1 = txtUsuario.getText();
        String senha1 = String.copyValueOf(cmpSenha.getPassword());

        String query = "SELECT * FROM `administrador_hotel` WHERE `Usuario`=? AND `Senha` =?\n SET GLOBAL time_zone = '-4:00';";

        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            ps.setString(1, usuario1);
            ps.setString(2, senha1);

            rs = ps.executeQuery();

            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Login com Exito");
                TelaInicial tli = new TelaInicial();
                tli.setVisible(true);
                tli.pack();
                tli.setLocationRelativeTo(null);
                TelaInicial.lblBemVindo.setText("Seja bem vindo, "+usuario1+"!");
                this.dispose();

            }else{
                JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos");
            }

        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try{
                ps.close();
            } catch (SQLException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnLoginActionPerformed

